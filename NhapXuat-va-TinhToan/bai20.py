# 20. Tính nhiều phép toán cùng lúc

a = float(input("Hãy nhập a: "))
b = float(input("Hãy nhập b: "))

print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} * {a} = {a * a}")
print(f"{a} // {b} = {a // b}")
print(f"{a} % {b} = {a % b}")